    public function update(Request $request)
    {
        //更新学分
        $Student = new Student;
        Student::where('id', $request->id)->increment('score',$request->num);
        //记录操作历史
        $Score = new Score;
        $Score->sid = $request->id;
        $Score->num = $request->num;
        $Score->reason = $request->reason;
        $Score->save();
